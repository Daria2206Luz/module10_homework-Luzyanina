let x;
x = 2;

if (typeof x == "number") {
  console.log("x - число");
} else if (typeof x == "string") {
  console.log("x - сторка");
} else if (typeof x == "boolean") {
  console.log("x - логический тип");
} else {
  console.log("Тип x не определён");
}

